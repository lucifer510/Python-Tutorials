# Operators In Python
# Arithmetic Op
# Assignment Op
# Comparison Op
# Logical Op
# Identity Op
# Membership Op
# Bitwise Op

# Arithmetic Operator
print(5 + 6)
print(5 - 6)
print(5 * 6)
print(5 ** 6)  # --> ** Is Power Operator
print(5 / 6)
print(45 // 6)  # --> // is Floor Divison Operator
print(5 % 6)

# Assignment Operators
x = 5
print(x)
x += 7
print(x)
x -= 5
print(x)
x /= 2
print(x)
x %= 7
print(x)

# Comparison Operators
i = 12
print(i == 9)
print(i != 9)
print(i > 9)
print(i >= 9)
print(i < 9)
print(i <= 9)

# Logical Operators
a = True
b = False
print(a and a)
print(a and b)
print(a or a)
print(a or b)


# Identity Operator
a = True
b = False
print(a is not a)
print(a is not b)
print(a is a)
print(a is b)


# Membership Operators
list = [1,2,3,6,4,8,9,10,54,23]
print(32 in list)
print(23 in list)

# Bitwise Operators

print( 0 & 1)
print( 1 & 1)
print( 1 & 0)
print( 0 & 0)

print( 0 | 0)
print( 0 | 1)
print( 1 | 1)
print( 1 | 0)